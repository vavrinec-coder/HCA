import fs from "node:fs";

const content = fs.readFileSync("public/custom-functions.js", "utf8");

if (!content.includes("Generated by scripts/build-custom-functions.mjs")) {
  throw new Error("public/custom-functions.js is not marked as generated.");
}

for (const forbidden of [
  "DIAG_LOAD_DETAIL",
  "DIAG_BACKEND",
  "associate(\"DIAG\"",
  "associate('DIAG'",
]) {
  if (content.includes(forbidden)) {
    throw new Error(
      `Generated custom-functions.js contains forbidden diagnostic: ${forbidden}`
    );
  }
}
